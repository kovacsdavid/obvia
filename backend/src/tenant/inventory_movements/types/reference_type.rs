use crate::common::types::{ValueObject, ValueObjectable};
use serde::{Deserialize, Serialize};
use std::fmt::Display;

#[derive(Debug, PartialEq, Clone, Serialize)]
pub struct ReferenceType(pub String);

impl ValueObjectable for ReferenceType {
    type DataType = String;

    fn validate(&self) -> Result<(), String> {
        match self.0.as_str() {
            "worksheets" => Ok(()),
            _ => Err(String::from("Hibás referencia típus")),
        }
    }

    fn get_value(&self) -> &Self::DataType {
        &self.0
    }
}

impl Display for ReferenceType {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "{}", self.0)
    }
}

impl<'de> Deserialize<'de> for ValueObject<ReferenceType> {
    fn deserialize<D>(deserializer: D) -> Result<Self, D::Error>
    where
        D: serde::Deserializer<'de>,
    {
        let s = String::deserialize(deserializer)?;
        ValueObject::new(ReferenceType(s)).map_err(serde::de::Error::custom)
    }
}
